// 두 수의 합
function solution1(num1, num2) {
  return num1 + num2;
}

solution1(2, 3);
solution1(100, 2);

// 두 수의 차
function solution2(num1, num2) {
  return num1 - num2;
}

solution2(2, 3);
solution2(100, 2);

// 두 수의 곱
function solution3(num1, num2) {
  let answer = num1 * num2;
  return answer;
}

solution3(3, 4);
solution3(27, 19);

// 몫 구하기
function solution4(num1, num2) {
  return Math.floor(num1 / num2);
}

solution4(10, 5);
solution4(7, 2);
